package practice;

public class NestedLoop {
    public static void main(String[] args) {
        //print 1 ~ 5 5 times
        for (int j = 1; j <= 5; j++) {

            for (int i = 1; i <= 5; i++) {
                System.out.println(i + "");
            }
            System.out.println();
        }


    }

}